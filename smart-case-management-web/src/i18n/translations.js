export const useTranslation = (language = 'en') => {
  const t = (key) => {
    const keys = key.split('.');
    let val = translations[language];
    for (const k of keys) val = val?.[k];
    if (val) return val;
    
    // Fallback to English
    let fallback = translations['en'];
    for (const k of keys) fallback = fallback?.[k];
    return fallback || key;
  };
  return { t };
};
